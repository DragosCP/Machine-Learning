# Dimensionality Reduction!
# In classifications, we worked with datasets composed of only two independent variables. We did this for two reasons
# 1. Because we needed 2 dimensions to better visualise how ML models worked
# 2. Because whatever is the original number of our independent variables, we can often end up with 2 independent variable by applying an appropriate Dimensionality Reduction Technique.

# There are 2 types of Dimensionality Reduction techniques:
# 1. Feature Selection
# 2. Feature Extraction

# Feature Selection techniques are Backward Elimination, Forward Selection, Bidirectional Elimination, Score Comparison and more. We covered these techniques in 01_Regression

# In this part we will cover the following Feature Extraction techniques:
# 1. Principal Component Analysis (PCA)
# 2. Linear Discriminant Analysis (LDA)
# 3. Kernel PCA

